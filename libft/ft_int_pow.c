#include "libft.h"

long long	ft_int_pow(long long x, int pow)
{
	int	i;
	int	z;

	i = 1;
	z = x;
	if (pow == 0)
		return (1);
	if (pow == 1)
		return (x);
	while (i++ < pow)
		x *= z;
	return (x);
}
