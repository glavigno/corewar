#include "libft.h"

int		ft_tabdel(char ***tab)
{
	int		i;

	i = ft_tab_len(*tab);
	i--;
	while (i > -1)
	{
		free((*tab)[i]);
		(*tab)[i] = NULL;
		i--;
	}
	free(*tab);
	return (0);
}
