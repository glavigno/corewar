#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
		if (c == '\0' && s[i + 1] == '\0')
			return (&((char*)s)[i + 1]);
	}
	while (i >= 0)
	{
		if (s[i] == c)
			return (&((char*)s)[i]);
		else
			i--;
	}
	return (0);
}
